//add
function add(a, b){
    return a + b;
}

//subtract
function subtract(a, b){
    return a-b;
}

//multiply
function multiply(a, b){
    return a*b;
}

//divide
function divide(a, b){
    return a/b;
}

//increment
function increment(a){
    return ++a;
}

//decrement
function decrement(a){
    return --a;
}

//makeInt(string)
function makeInt(n){
    return parseInt(n, 10);
}

//preserveDecimal(string)
function preserveDecimal(n){
    return parseFloat(n);
}
